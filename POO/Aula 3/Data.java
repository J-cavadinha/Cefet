public class Data {
    private int dia;
    private int mes;
    private int ano;

    public int getdia() {
        return this.dia;
    }

    public void setdia(int dia) {
        this.dia = dia;
    }

    public int getmes() {
        return this.mes;
    }

    public void setmes(int mes) {
        this.mes = mes;
    }

    public int getano() {
        return this.ano;
    }

    public void setano(int ano) {
        this.ano = ano;
    }
}
